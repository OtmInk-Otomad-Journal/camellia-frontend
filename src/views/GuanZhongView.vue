<script setup>
import GuanZhongInfo from '../components/GuanZhongInfo.vue'
import { data, fun } from '../data/MainView_data.js'
import { gsap } from 'gsap'
import { onMounted } from 'vue'
import { ScrollToPlugin } from 'gsap/all'
import BackgroundImage from '../components/BackgroundImage.vue'
import TransitionImage from '../components/TransitionImage.vue'

gsap.registerPlugin(ScrollToPlugin)

// “全局”变量，既方便函数内调用，也方便外面调用。

let tl_1 = gsap.timeline()
let tl_2 = gsap.timeline()
let tl_3 = gsap.timeline()
let tl_4 = gsap.timeline()
let tl_5 = gsap.timeline()
// let tls = []

const videoRef = ref()

// 动画 使用css选择器
function animate() {
  tl_1.from('.video-box', {
    duration: 1,
    rotationX: 90,
    ease: 'expo.out'
  })
  tl_1.to(
    '.main-left',
    {
      duration: 1,
      x: 1800,
      ease: 'expo.in'
    },
    data.value.full_time - 1
  ),
    tl_2.from(
      '.main-info',
      {
        duration: 0.8,
        y: 50,
        rotationX: 90,
        ease: 'expo.out'
      },
      0
    ),
    tl_2.from(
      '.main-left',
      {
        duration: 0.8,
        x: -1800,
        ease: 'expo.out'
      },
      0
    ),
    tl_2.from(
      '.chip',
      {
        duration: 0.8,
        delay: 0.4,
        stagger: 0.06,
        y: 50,
        opacity: 0,
        ease: 'expo.out'
      },
      0
    ),
    tl_2.from(
      '.main-title',
      {
        duration: 0.8,
        delay: 0.5,
        y: 50,
        opacity: 0,
        ease: 'expo.out'
      },
      0
    )
  tl_3.to(
    '.main-progress',
    {
      duration: data.value.full_time,
      width: '100%',
      ease: 'linear'
    },
    0
  )
  tl_4.from('.logo', { duration: 0.75, x: -1000, ease: 'expo.out' })
  tl_4.to(
    '.logo',
    {
      duration: 1,
      x: -1000,
      ease: 'expo.in'
    },
    data.value.full_time - 1
  )
  // 过渡用图动画
  tl_5.to('.transition-image', {
    opacity: 0,
    duration: 1
  })
  tl_5.to(
    '.transition-image',
    {
      opacity: 1,
      duration: 0.5
    },
    data.value.full_time - 0.5
  )
}

//// 全局函数 统一写在这
function seek_frame(frame, fps, start_time) {
  tl_1.seek(frame / fps)
  tl_2.seek(frame / fps)
  tl_3.seek(frame / fps)
  tl_4.seek(frame / fps)
  tl_5.seek(frame / fps)
  videoRef.value.currentTime = start_time + frame / fps
}

//// 全局函数 统一写在这
window['seek_frame'] = (frame, fps, start_time) => {
  seek_frame(frame, fps, start_time)
}

window['inject'] = (obj) => {
  fun(obj).then(() => {
    animate()
  })
  tl_1.pause()
  tl_2.pause()
  tl_3.pause()
  tl_4.pause()
  tl_5.pause()
}

window['inject_wvc'] = (obj) => {
  fun(obj).then(() => {
    animate()
  })
}

onMounted(() => {
  fun(data.value)
})

// 测试专用函数

var test_num = ref(0)
window['test'] = () => {
  if (test_num.value == 0) {
    animate()
    test_num.value += 1
  }
  tl_1.restart()
  tl_2.restart()
  tl_3.restart()
  tl_4.restart()
  tl_5.restart()
}
</script>

<template>
  <img class="logo" src="/2024LOGO.png" />
  <button class="test-button" v-if="test_num != 0" onclick="test()">重播动画</button>
